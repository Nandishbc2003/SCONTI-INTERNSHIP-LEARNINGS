// String functions
let greeting = "Hello, World!";

let upperCaseGreeting = greeting.toUpperCase();
let lowerCaseGreeting = greeting.toLowerCase();
let subString = greeting.substring(0, 5);
let indexOfComma = greeting.indexOf(',');

// Displaying the results
console.log("Upper Case Greeting:", upperCaseGreeting);
console.log("Lower Case Greeting:", lowerCaseGreeting);
console.log("Substring:", subString);
console.log("Index of Comma:", indexOfComma);
